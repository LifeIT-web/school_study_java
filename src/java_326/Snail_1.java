public class Snail_1 
{
	public static void main(String[] args) 
	{
/* 13미터 우물속에 달팽이가 있는데 낮에는 3미터 오르고 밤에는 1미터 내려가
 달팽이 탈출일은?*/

		int count = 0;
		int snail = 0;

		while (true)
		{
			snail += 3;
			count++;
			if (snail >= 13)
			{
				break;
			}
			snail -= 1;

		}
		System.out.println("오른 거리는  " + snail);
		System.out.println("시간는  " + count);
	}
}
