public class Continue_1 
{
	public static void main(String[] args) 
	{
		int sum = 0;

		for (int i = 0; i < 10  ; i++ )
		{
			if (i % 2 == 0)
			{
				continue; //  반복문의 끝으로 감.( if 는 반복문이 아니라 for문이 반복문이기에 올라감)
			}
			sum += i;
			System.out.print(i + "\t");
		}
		System.out.println(" \n sum = " + sum);
	}
}
